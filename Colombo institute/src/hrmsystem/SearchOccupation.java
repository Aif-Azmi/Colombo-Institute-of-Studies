    private void loadOccupationDetails() {

        try {

            DefaultTableModel defaultTableModel = (DefaultTableModel) jTable1.getModel();
            defaultTableModel.setRowCount(0);
            String searchquery = "select * from occupations";
            ResultSet resultSet = DB.search(searchquery);
            while (resultSet.next()) {
                Vector v = new Vector();
                v.add(resultSet.getString("id"));
                v.add(resultSet.getString("occupationname"));
                v.add(resultSet.getString("salary"));
                defaultTableModel.addRow(v);
            }

            // defaultTableModel.setRowCount(0);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }
